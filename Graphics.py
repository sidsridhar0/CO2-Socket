import matplotlib.pyplot as plt
import numpy as np


class Graphic():
    def __init__(self, x, y):
        self.ind = x
        self.dep = y

    def makeGraphic(self):
        x = self.ind
        y = self.dep
        x = x[0]
        y = y[0]
        name = y[0]
        yname = "CO2 level"
        xname = "Year"
        x = list(x[1:])
        x = [int(i) for i in x]
        y = list(y[1:])
        y = [float(i) for i in y]
        # print(x, xname)
        # print(y, name)

        plt.scatter(x, y)
        fit = np.polyfit(x, y, 1)
        func = np.poly1d(fit)
        plt.plot(x, func(x), '--k')
        plt.xlabel(xname)
        plt.ylabel(yname)
        plt.title(name + " CO2 Graph")
        plt.show()

